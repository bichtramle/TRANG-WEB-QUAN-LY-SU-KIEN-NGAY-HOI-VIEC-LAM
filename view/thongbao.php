Căn cứ Kế hoạch tổ chức “Ngày hội việc làm năm 2022” của Học viện Nông nghiệp Việt Nam;

Với mong muốn tạo môi trường để nhà tuyển dụng lao động lựa chọn và tuyển dụng được nhân lực phù hợp với vị trí cần tuyển dụng. Mặt khác, để tạo cơ hội cho sinh viên, ứng viên có thể kết nối với các nhà tuyển dụng có tiềm năng, giúp sinh viên có định hướng học tập và rèn luyện tốt hơn để tìm được việc làm thích hợp sau khi tốt nghiệp. Học viện Nông nghiệp Việt Nam sẽ tổ chức “Ngày hội việc làm năm 2022” vào ngày 16 tháng 8 năm 2022.

Để công tác tổ chức được thực sự hiệu quả, đáp ứng tối đa các nhu cầu, nguyện vọng của sinh viên đang học tập, sinh viên chuẩn bị tốt nghiệp và sinh viên đã tốt nghiệp, Giám đốc Học viện yêu cầu Ban chủ nhiệm các Khoa, Ban Thường vụ Đoàn Thanh niên, Ban Thư ký Hội sinh viên thông báo cho sinh viên tham gia khảo sát nhu cầu tìm kiếm việc làm thông qua mẫu Phiếu đăng ký tham dự Ngày hội việc làm 2022 như sau:

